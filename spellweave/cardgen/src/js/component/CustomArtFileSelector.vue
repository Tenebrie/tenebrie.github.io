<template>
	<base-file-selector @select="onFileSelected" />
</template>

<script>
	import BaseFileSelector from "Component/BaseFileSelector.vue";

	export default {
		components: {
			BaseFileSelector,
		},
		methods: {
			onFileSelected: function(file) {
				let reader = new FileReader();

				reader.onloadend = () => {
					this.$store.commit('cardState/setCustomImageData', reader.result);
				};
				reader.readAsDataURL(file);
			}
		},
	}
</script>